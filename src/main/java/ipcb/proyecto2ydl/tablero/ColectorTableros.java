    public void actualizar(){
        String nombresTab="";
        try {
        for (int i = 0; i < listaTableros.getSize(); i++) {
            
                Tablero tablero = listaTableros.getContenido(i);
                nombresTab += " "  + (i+1) + " " +tablero.getId() + "\n";
        }} catch (Exception e) {
            }
        displayTableros.setText(nombresTab);
    }

