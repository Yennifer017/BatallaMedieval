    private void bEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEliminarActionPerformed
        try {
            int tableroNo = (Integer.parseInt(seleccion.getText())) - 1;
            listaTableros.eliminar(tableroNo);
            Archivo<LinkedListY<Tablero>> archivo = new Archivo<>();
            archivo.guardarArchivoBin(AdmiTableros.ARCHIVO_TABLEROS, listaTableros);
            JOptionPane.showMessageDialog(null, "Se ha eliminado el tablero " + (tableroNo+1));
            actualizar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_bEliminarActionPerformed


