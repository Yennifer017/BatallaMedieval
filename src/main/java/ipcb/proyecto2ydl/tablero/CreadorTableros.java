    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        try {
            Dimensiones dimensiones = creadorT.leerDimensiones(dimX, dimY);
            currentId = userId.getText();
            if(!currentId.equals("")){
                mostrarDatos();
                displayTab.removeAll();
                creadorT.inicializarCreador(dimensiones, displayTab);
            }else{
                JOptionPane.showMessageDialog(null, "Tienes que ingresar un id valido");
            }
        } catch (LecturaException  e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_bAceptarActionPerformed

