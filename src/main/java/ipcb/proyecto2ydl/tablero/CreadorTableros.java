    private void bSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSaveActionPerformed
        Tablero tablero = new Tablero(currentId, creadorT.getContentTabCreado());
        listaTableros.add(tablero);
        ocultarDatos();
        Archivo<LinkedListY<Tablero>> archivo = new Archivo<>();
        archivo.guardarArchivoBin(pestanaAnterior.ARCHIVO_TABLEROS, listaTableros);
        JOptionPane.showMessageDialog(null, "Se ha guardado el tablero" + currentId);
    }//GEN-LAST:event_bSaveActionPerformed


